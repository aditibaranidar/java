class Literals
{
    public static void main(String a[])
    {
        // Int Literals 
        int num1 = 585;
        System.out.println(num1);

        // Double Literals
        double num2 = 56;  // Integer value can be assigned to double variable, will be converted to float
        System.out.println(num2);

        // Boolean Literals
        boolean status = true; // Boolean literals cannot convert from int or other types
        System.out.println(status);

        // Character Literals
        char c = 'a'; // Use the command c++ to increment character value
        System.out.println(c);

    }
}
